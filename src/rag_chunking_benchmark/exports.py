"""Export helpers for JSON, CSV, and Markdown reports."""

from .common import *
from .schemas import MethodMetrics
from .utils import safe_divide

def export_json(results: Dict) -> str:
    return json.dumps(results, indent=2, default=str)

def export_leaderboard_csv(method_metrics: List[MethodMetrics]) -> str:
    rows = []
    for m in method_metrics:
        rows.append({
            "rank": m.rank, "method": m.method,
            "final_score": m.final_score,
            "avg_relevance": m.avg_relevance,
            "avg_correctness": m.avg_correctness,
            "avg_faithfulness": m.avg_faithfulness,
            "total_pipeline_ms": m.total_pipeline_time_ms,
            "avg_context_tokens": m.avg_context_tokens,
            "total_chunks": m.total_chunks,
        })
    return pd.DataFrame(rows).to_csv(index=False)

def export_per_question_csv(qa_records: List[Dict]) -> str:
    return pd.DataFrame(qa_records).to_csv(index=False)

def export_markdown_report(doc_name: str, questions: List[Dict],
                           method_metrics: List[MethodMetrics],
                           qa_records: List[Dict]) -> str:
    lines = [
        f"# RAG Chunking Benchmark Report",
        f"",
        f"**Document:** {doc_name}  ",
        f"**Questions tested:** {len(questions)}  ",
        f"**Chunking methods evaluated:** {', '.join([m.method for m in method_metrics])}",
        f"",
        f"---",
        f"",
        f"## 🏆 Leaderboard",
        f"",
        f"| Rank | Method | Score | Correctness | Faithfulness | Latency (ms) |",
        f"|------|--------|-------|-------------|--------------|-------------|",
    ]
    for m in method_metrics:
        lines.append(
            f"| {m.rank} | {m.method} | {m.final_score:.3f} | "
            f"{m.avg_correctness:.3f} | {m.avg_faithfulness:.3f} | "
            f"{m.total_pipeline_time_ms:.0f} |"
        )
    best = method_metrics[0] if method_metrics else None
    lines += [
        f"",
        f"---",
        f"",
        f"## 🥇 Best Method: {best.method if best else 'N/A'}",
        f"",
        f"{best.recommendation if best else ''}",
        f"",
        f"**Strengths:** {', '.join(best.strengths) if best else ''}  ",
        f"**Weaknesses:** {', '.join(best.weaknesses) if best else ''}",
        f"",
        f"---",
        f"",
        f"## 📊 Method Comparison",
        f"",
    ]
    for m in method_metrics:
        lines += [
            f"### {m.method} (Rank #{m.rank})",
            f"- **Score:** {m.final_score:.3f}",
            f"- **Chunks:** {m.total_chunks}  avg size: {m.avg_chunk_size_words:.0f} words",
            f"- **Avg relevance:** {m.avg_relevance:.3f}",
            f"- **Avg correctness:** {m.avg_correctness:.3f}",
            f"- **Avg faithfulness:** {m.avg_faithfulness:.3f}",
            f"- **Pipeline time:** {m.total_pipeline_time_ms:.0f} ms",
            f"- **Strengths:** {', '.join(m.strengths)}",
            f"- **Weaknesses:** {', '.join(m.weaknesses)}",
            f"",
        ]
    lines += [
        f"---",
        f"",
        f"## ❓ Per-Question Answer Comparison",
        f"",
    ]
    q_grouped: Dict[str, List[Dict]] = {}
    for rec in qa_records:
        q_grouped.setdefault(rec["question_id"], []).append(rec)
    for qid, recs in q_grouped.items():
        lines.append(f"### {qid}: {recs[0]['question_text']}")
        lines.append(f"")
        lines.append(f"| Method | Answer | Correctness | Faithfulness |")
        lines.append(f"|--------|--------|-------------|--------------|")
        for rec in recs:
            ans = str(rec.get("answer", ""))[:120].replace("|", "/")
            lines.append(f"| {rec['method']} | {ans}… | "
                         f"{rec.get('correctness', 0):.2f} | "
                         f"{rec.get('faithfulness', 0):.2f} |")
        lines.append(f"")
    lines += [
        f"---",
        f"",
        f"## ⚠️ Limitations",
        f"",
        f"- Heuristic scoring approximates LLM-judge quality when no API key is provided.",
        f"- Results depend heavily on document quality and question phrasing.",
        f"- Semantic chunking quality depends on the embedding model.",
        f"",
        f"*Generated by RAG Chunking Benchmark System*",
    ]
    return "\n".join(lines)
